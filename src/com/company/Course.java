package com.company;

public class Course {

    private String name;
    private int number;
    private String nameOfTeacher;
    private String srNameOfTeacher;
    private String date;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getNameOfTeacher() {
        return nameOfTeacher;
    }

    public void setNameOfTeacher(String nameOfTeacher) {
        this.nameOfTeacher = nameOfTeacher;
    }

    public String getSrNameOfTeacher() {
        return srNameOfTeacher;
    }

    public void setSrNameOfTeacher(String srNameOfTeacher) {
        this.srNameOfTeacher = srNameOfTeacher;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
